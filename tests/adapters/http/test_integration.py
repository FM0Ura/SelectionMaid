"""End-to-end integration tests for SelectionMaid HTTP adapter.

These tests use the real DocumentConverter (no mocks) and real adversarial
fixtures to verify service reliability under hostile input and concurrent load.

Test coverage:
- Adversarial inputs: corrupt, empty, spoofed, password-protected, large PDFs
- Liveness: /health returns 200 after a failing /ingest request
- Concurrency: 5 simultaneous POST /ingest requests all return 200
- Cleanup audit: /tmp/selectionmaid_* tempfiles are removed after processing

Fixture generation:
  Adversarial fixtures are generated by tests/fixtures/generate_adversarial.py.
  This module ensures fixtures exist before tests run via session-scoped fixtures.

Notes on expected status codes:
  - corrupt.pdf:    magic detects application/octet-stream != application/pdf -> 422
  - empty.pdf:      magic detects application/x-empty != application/pdf       -> 422
  - spoofed.pdf:    magic detects text/plain != application/pdf                -> 422
  - protected.pdf:  passes magic (valid PDF headers), Docling fails to decrypt  -> 500
  - large_sample.pdf: ~40MB < 50MB limit; Docling processes it                -> 200 or 500
  - sample.pdf:     valid PDF, Docling extracts successfully                   -> 200
"""
from __future__ import annotations

import asyncio
import glob
import subprocess
import sys
from contextlib import asynccontextmanager
from datetime import datetime, timezone
from pathlib import Path
from typing import AsyncIterator

import httpx
import pytest
from fastapi import FastAPI

# ---------------------------------------------------------------------------
# Directory constants
# ---------------------------------------------------------------------------

FIXTURES_DIR = Path(__file__).parent.parent.parent / "fixtures"
ADV_DIR = FIXTURES_DIR / "adversarial"


# ---------------------------------------------------------------------------
# Session-scoped fixtures
# ---------------------------------------------------------------------------


@pytest.fixture(scope="session", autouse=True)
def ensure_adversarial_fixtures() -> None:
    """Generate adversarial fixtures if they do not exist.

    Runs tests/fixtures/generate_adversarial.py once per test session.
    If generation fails (e.g., missing sample.pdf), the session continues
    and individual tests that depend on missing fixtures will be skipped.
    """
    generator = FIXTURES_DIR / "generate_adversarial.py"
    expected = [
        ADV_DIR / "corrupt.pdf",
        ADV_DIR / "empty.pdf",
        ADV_DIR / "spoofed.pdf",
        ADV_DIR / "protected.pdf",
        ADV_DIR / "large_sample.pdf",
    ]
    # Only re-generate if any fixture is missing
    if all(p.exists() for p in expected):
        return
    result = subprocess.run(
        [sys.executable, str(generator)],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        # Non-fatal: individual tests will skip if their fixture is missing
        pass


@pytest.fixture(scope="session")
def real_app() -> FastAPI:
    """Build a FastAPI app wired with the real DocumentConverter.

    The router is included at app construction time (not inside lifespan) so
    that FastAPI's routing layer is ready before the first request arrives.
    This mirrors the pattern used in test_router.py for the concurrency test.

    Docling imports are deferred inside the lifespan body so that model loading
    does not occur during fixture collection.
    """
    from selection_maid.adapters.http.router import build_router
    from selection_maid.config import get_config

    config = get_config()

    # Build the real service using the same wiring as app.py
    from docling.datamodel.base_models import InputFormat
    from docling.datamodel.pipeline_options import PdfPipelineOptions
    from docling.document_converter import (
        DocumentConverter,
        PdfFormatOption,
        WordFormatOption,
    )
    from docling.pipeline.simple_pipeline import SimplePipeline
    from docling.pipeline.standard_pdf_pipeline import StandardPdfPipeline

    from selection_maid.adapters.chunker.markdown import build_markdown_chunker
    from selection_maid.adapters.enricher.default import build_metadata_enricher
    from selection_maid.adapters.extractor.docling import build_docling_adapter
    from selection_maid.adapters.filter.heuristic import build_heuristic_filter
    from selection_maid.service import ExtractionService

    pdf_options = PdfPipelineOptions(
        do_ocr=False,
        do_table_structure=True,
    )
    converter = DocumentConverter(
        allowed_formats=[InputFormat.PDF, InputFormat.DOCX, InputFormat.HTML],
        format_options={
            InputFormat.PDF: PdfFormatOption(
                pipeline_cls=StandardPdfPipeline,
                pipeline_options=pdf_options,
            ),
            InputFormat.DOCX: WordFormatOption(pipeline_cls=SimplePipeline),
        },
    )

    extractor = build_docling_adapter(converter)
    filter_ = build_heuristic_filter(config.filter)
    chunker = build_markdown_chunker(config.chunker)
    enricher = build_metadata_enricher(config.enricher)

    service = ExtractionService(
        extractor=extractor,
        filter_=filter_,
        chunker=chunker,
        enricher=enricher,
    )

    router = build_router(service, config)

    @asynccontextmanager
    async def _lifespan(app: FastAPI) -> AsyncIterator[None]:
        app.state.start_time = datetime.now(timezone.utc)
        yield

    app = FastAPI(lifespan=_lifespan)
    app.include_router(router)

    # httpx.ASGITransport does not trigger the FastAPI lifespan, so we must
    # set app.state.start_time directly here.  The /health endpoint reads
    # request.app.state.start_time (D-86) and would raise AttributeError
    # without this pre-initialization.
    app.state.start_time = datetime.now(timezone.utc)

    return app


