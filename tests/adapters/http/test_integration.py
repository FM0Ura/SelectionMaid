class TestConcurrencyStress:
    """Verify the service handles concurrent requests correctly."""

    @pytest.mark.asyncio
    async def test_concurrency_stress_five_requests(self, real_app: FastAPI) -> None:
        """5 simultaneous POST /ingest requests for sample.pdf all return 200.

        Uses asyncio.gather to fire 5 concurrent requests against the real
        service. All must succeed to verify that:
        - run_in_threadpool correctly offloads CPU-bound Docling work
        - Tempfile handling is thread-safe (each request gets its own file)
        - No shared state is corrupted by concurrent access
        """
        pdf_bytes = _fixture_or_skip(FIXTURES_DIR / "sample.pdf")
        transport = httpx.ASGITransport(app=real_app)

        async def _post_ingest(client: httpx.AsyncClient, idx: int) -> int:
            response = await client.post(
                "/ingest",
                files={
                    "file": (
                        f"sample_{idx}.pdf",
                        pdf_bytes,
                        "application/pdf",
                    )
                },
            )
            return response.status_code

        async with httpx.AsyncClient(
            transport=transport,
            base_url="http://test",
            timeout=300.0,
        ) as client:
            statuses = await asyncio.gather(
                *[_post_ingest(client, i) for i in range(5)]
            )

        failures = [s for s in statuses if s != 200]
        assert not failures, (
            f"Expected all 5 concurrent requests to return 200, "
            f"but got: {list(statuses)}"
        )


