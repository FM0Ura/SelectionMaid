class TestLiveness:
    """Verify /health remains available after processing failures."""

    @pytest.mark.asyncio
    async def test_health_ok_after_failing_ingest(self, real_app: FastAPI) -> None:
        """GET /health returns 200 even after a failed /ingest request.

        Sends a spoofed PDF (guaranteed to trigger 422) and then immediately
        checks /health — the service must still report status 'ok'. This
        verifies that error handling in the ingest path does not corrupt
        application state.
        """
        spoofed_bytes = _fixture_or_skip(ADV_DIR / "spoofed.pdf")
        transport = httpx.ASGITransport(app=real_app)
        async with httpx.AsyncClient(
            transport=transport, base_url="http://test"
        ) as client:
            # Trigger a failure
            bad_response = await client.post(
                "/ingest",
                files={"file": ("spoofed.pdf", spoofed_bytes, "application/pdf")},
            )
            assert bad_response.status_code == 422  # confirm failure occurred

            # Service must still be alive
            health_response = await client.get("/health")
        assert health_response.status_code == 200
        assert health_response.json()["status"] == "ok"


