class TestTempfileCleanupAudit:
    """Verify that /tmp/selectionmaid_* tempfiles are cleaned up after requests."""

    @pytest.mark.asyncio
    async def test_tempfile_cleanup_audit(self, real_app: FastAPI) -> None:
        """No selectionmaid_* tempfiles remain in /tmp after request completes.

        Records the count of /tmp/selectionmaid_* files before the request and
        verifies the count returns to baseline after the response. Tests both
        success (sample.pdf) and error (spoofed.pdf) paths to ensure the
        finally-block cleanup in the router fires in all cases.
        """
        pdf_bytes = _fixture_or_skip(FIXTURES_DIR / "sample.pdf")
        spoofed_bytes = _fixture_or_skip(ADV_DIR / "spoofed.pdf")
        transport = httpx.ASGITransport(app=real_app)

        async with httpx.AsyncClient(
            transport=transport, base_url="http://test", timeout=120.0
        ) as client:
            # Baseline: how many selectionmaid tempfiles exist right now
            baseline = _count_selectionmaid_tmpfiles()

            # Test cleanup on success path
            success_response = await client.post(
                "/ingest",
                files={"file": ("sample.pdf", pdf_bytes, "application/pdf")},
            )
            after_success = _count_selectionmaid_tmpfiles()

            # Test cleanup on error path (422 — magic rejected, no tempfile created)
            error_response = await client.post(
                "/ingest",
                files={"file": ("spoofed.pdf", spoofed_bytes, "application/pdf")},
            )
            after_error = _count_selectionmaid_tmpfiles()

        assert success_response.status_code == 200, (
            f"Success path returned {success_response.status_code}: {success_response.text}"
        )
        assert error_response.status_code == 422, (
            f"Expected 422 for spoofed PDF, got {error_response.status_code}"
        )

        # After each request, the tempfile count must return to baseline
        assert after_success == baseline, (
            f"Tempfile leak after successful ingest: "
            f"before={baseline}, after={after_success}"
        )
        assert after_error == baseline, (
            f"Tempfile leak after failed ingest: "
            f"before={baseline}, after={after_error}"
        )
