def _fixture_or_skip(path: Path) -> bytes:
    """Read fixture file bytes or skip the test if the fixture is missing."""
    if not path.exists():
        pytest.skip(f"Fixture not found: {path}")
    return path.read_bytes()


def _count_selectionmaid_tmpfiles() -> int:
    """Count current /tmp/selectionmaid_* files."""
    return len(glob.glob("/tmp/selectionmaid_*"))


